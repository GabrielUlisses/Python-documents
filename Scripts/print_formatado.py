value = 10.0
print("%i"%(value)) # Imprime como inteiro
print("%i"%(value * 10.6)) # Imprime como inteiro
print("%f"%(value)) # Imprime como Float
print("%.2f"%(value)) # Imprime como Float
print("%f"%(10 + 5)) # Imprime como Float
print("%s"%(value)) # Imprime como String
print("%r"%(value)) # Imprime como String
print("%c"%(value)) # imprime o valor segundo a tabela ASCII
print(f'{value}')
